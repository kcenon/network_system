#include <gtest/gtest.h>

#include "kcenon/network/facade/http_facade.h"
#include "internal/http/http_client.h"
#include "internal/http/http_server.h"

#include <chrono>
#include <memory>
#include <stdexcept>
#include <string>
#include <thread>

using namespace kcenon::network::facade;
using namespace kcenon::network::core;
using namespace kcenon::network::internal;

class HttpFacadeTest : public ::testing::Test
{
protected:
	void SetUp() override
	{
		facade_ = std::make_unique<http_facade>();
	}

	void TearDown() override
	{
		if (server_)
		{
			server_->stop();
		}
	}

	std::unique_ptr<http_facade> facade_;
	std::shared_ptr<http_client> client_;
	std::shared_ptr<http_server> server_;
};

// ============================================================================
// Client Configuration Validation Tests
// ============================================================================

TEST_F(HttpFacadeTest, CreateClientThrowsOnNegativeTimeout)
{
	http_facade::client_config config{
		.timeout = std::chrono::milliseconds(-1000),
	};

	EXPECT_THROW(facade_->create_client(config), std::invalid_argument);
}

TEST_F(HttpFacadeTest, CreateClientThrowsOnZeroTimeout)
{
	http_facade::client_config config{
		.timeout = std::chrono::milliseconds(0),
	};

	EXPECT_THROW(facade_->create_client(config), std::invalid_argument);
}

TEST_F(HttpFacadeTest, CreateClientWithDefaultTimeout)
{
	http_facade::client_config config{};

	client_ = facade_->create_client(config);

	ASSERT_NE(client_, nullptr);
	EXPECT_EQ(client_->get_timeout(), std::chrono::seconds(30));
}

TEST_F(HttpFacadeTest, CreateClientWithCustomTimeout)
{
	http_facade::client_config config{
		.timeout = std::chrono::seconds(10),
	};

	client_ = facade_->create_client(config);

	ASSERT_NE(client_, nullptr);
	EXPECT_EQ(client_->get_timeout(), std::chrono::seconds(10));
}

// ============================================================================
// Server Configuration Validation Tests
// ============================================================================

TEST_F(HttpFacadeTest, CreateServerThrowsOnInvalidPortZero)
{
	http_facade::server_config config{
		.port = 0,
	};

	EXPECT_THROW(facade_->create_server(config), std::invalid_argument);
}

TEST_F(HttpFacadeTest, CreateServerWithAutoGeneratedId)
{
	http_facade::server_config config{
		.port = 8080,
	};

	server_ = facade_->create_server(config);

	ASSERT_NE(server_, nullptr);
}

TEST_F(HttpFacadeTest, CreateServerWithCustomId)
{
	http_facade::server_config config{
		.port = 8080,
		.server_id = "my-custom-server",
	};

	server_ = facade_->create_server(config);

	ASSERT_NE(server_, nullptr);
}

TEST_F(HttpFacadeTest, CreateMultipleServersWithUniqueAutoIds)
{
	http_facade::server_config config1{
		.port = 8080,
	};

	http_facade::server_config config2{
		.port = 8081,
	};

	auto server1 = facade_->create_server(config1);
	auto server2 = facade_->create_server(config2);

	ASSERT_NE(server1, nullptr);
	ASSERT_NE(server2, nullptr);
	EXPECT_NE(server1, server2);
}

