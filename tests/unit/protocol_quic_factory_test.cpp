// ============================================================================

class QuicConnectTest : public ::testing::Test
{
};

TEST_F(QuicConnectTest, ConnectWithEndpointReturnsNonNull)
{
	quic::quic_config config;
	config.server_name = "localhost";
	config.insecure_skip_verify = true;

	auto conn = quic::connect(
		unified::endpoint_info{"127.0.0.1", 0}, config, "connect-test");

	ASSERT_NE(conn, nullptr);
}

TEST_F(QuicConnectTest, ConnectWithUrlReturnsNonNull)
{
	quic::quic_config config;
	config.insecure_skip_verify = true;

	auto conn = quic::connect("quic://127.0.0.1:4433", config, "url-test");

	ASSERT_NE(conn, nullptr);
}

TEST_F(QuicConnectTest, ConnectWithInvalidUrlStillReturnsObject)
{
	quic::quic_config config;

	auto conn = quic::connect("invalid-url", config, "bad-url");

	// Should still return a connection object (will fail on connect attempt)
	ASSERT_NE(conn, nullptr);
}

