TEST_F(WebSocketFacadeTest, AutoGeneratedClientIdsAreUnique)
{
	websocket_facade::client_config config1{};
	websocket_facade::client_config config2{};

	std::shared_ptr<i_protocol_client> client1;
	std::shared_ptr<i_protocol_client> client2;

	ASSERT_NO_THROW({
		client1 = facade_->create_client(config1);
		client2 = facade_->create_client(config2);
	});

	ASSERT_NE(client1, nullptr);
	ASSERT_NE(client2, nullptr);
	// Each client is a distinct object
	EXPECT_NE(client1.get(), client2.get());

	// Cleanup
	client1->stop();
	client2->stop();
}

