// ============================================================================
// Type Safety Tests
// ============================================================================

TEST_F(WebSocketFacadeTest, CreateClientReturnsProtocolClientInterface)
{
	websocket_facade::client_config config{};

	auto client = facade_->create_client(config);

	// Verify it's a valid i_protocol_client
	ASSERT_NE(client, nullptr);
	// Verify interface methods are accessible
	EXPECT_FALSE(client->is_connected());
	EXPECT_FALSE(client->is_connected());

	client->stop();
}

TEST_F(WebSocketFacadeTest, CreateServerReturnsProtocolServerInterface)
{
	websocket_facade::server_config config{
		.port = 8080,
	};

	auto server = facade_->create_server(config);

	// Verify it's a valid i_protocol_server
	ASSERT_NE(server, nullptr);
	// Verify interface methods are accessible
	EXPECT_EQ(server->connection_count(), 0);

	server->stop();
}

