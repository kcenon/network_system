#include <gtest/gtest.h>

#include "kcenon/network/facade/websocket_facade.h"
#include "kcenon/network/http/websocket_client.h"
#include "kcenon/network/http/websocket_server.h"

#include <chrono>
#include <memory>
#include <stdexcept>
#include <string>
#include <thread>

using namespace kcenon::network::facade;
using namespace kcenon::network::core;
using namespace kcenon::network::internal;

class WebSocketFacadeTest : public ::testing::Test
{
protected:
	void SetUp() override
	{
		facade_ = std::make_unique<websocket_facade>();
	}

	void TearDown() override
	{
		if (server_)
		{
			server_->stop_server();
		}
		if (client_)
		{
			client_->stop_client();
		}
	}

	std::unique_ptr<websocket_facade> facade_;
	std::shared_ptr<messaging_ws_client> client_;
	std::shared_ptr<messaging_ws_server> server_;
};

// ============================================================================
// Client Configuration Validation Tests
// ============================================================================

TEST_F(WebSocketFacadeTest, CreateClientThrowsOnNegativePingInterval)
{
	websocket_facade::client_config config{
		.ping_interval = std::chrono::milliseconds(-1000),
	};

	EXPECT_THROW(facade_->create_client(config), std::invalid_argument);
}

TEST_F(WebSocketFacadeTest, CreateClientThrowsOnZeroPingInterval)
{
	websocket_facade::client_config config{
		.ping_interval = std::chrono::milliseconds(0),
	};

	EXPECT_THROW(facade_->create_client(config), std::invalid_argument);
}

TEST_F(WebSocketFacadeTest, CreateClientWithDefaultPingInterval)
{
	websocket_facade::client_config config{};

	client_ = facade_->create_client(config);

	ASSERT_NE(client_, nullptr);
	EXPECT_FALSE(client_->is_running());
}

TEST_F(WebSocketFacadeTest, CreateClientWithCustomPingInterval)
{
	websocket_facade::client_config config{
		.ping_interval = std::chrono::seconds(60),
	};

	client_ = facade_->create_client(config);

	ASSERT_NE(client_, nullptr);
	EXPECT_FALSE(client_->is_running());
}

TEST_F(WebSocketFacadeTest, CreateClientWithCustomClientId)
{
	websocket_facade::client_config config{
		.client_id = "custom_client_123",
	};

	client_ = facade_->create_client(config);

	ASSERT_NE(client_, nullptr);
	EXPECT_EQ(client_->client_id(), "custom_client_123");
}

TEST_F(WebSocketFacadeTest, CreateClientWithAutoGeneratedId)
{
	websocket_facade::client_config config{};

	client_ = facade_->create_client(config);

	ASSERT_NE(client_, nullptr);
	EXPECT_FALSE(client_->client_id().empty());
	EXPECT_EQ(client_->client_id().substr(0, 10), "ws_client_");
}

TEST_F(WebSocketFacadeTest, AutoGeneratedClientIdsAreUnique)
{
	websocket_facade::client_config config1{};
	websocket_facade::client_config config2{};

	auto client1 = facade_->create_client(config1);
	auto client2 = facade_->create_client(config2);

	ASSERT_NE(client1, nullptr);
	ASSERT_NE(client2, nullptr);
	EXPECT_NE(client1->client_id(), client2->client_id());
}

