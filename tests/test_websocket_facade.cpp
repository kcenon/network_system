#include <gtest/gtest.h>

#include "kcenon/network/facade/websocket_facade.h"
#include "kcenon/network/interfaces/i_protocol_client.h"
#include "kcenon/network/interfaces/i_protocol_server.h"

#include <chrono>
#include <memory>
#include <stdexcept>
#include <string>
#include <thread>
#include <vector>

using namespace kcenon::network::facade;
using namespace kcenon::network::interfaces;

class WebSocketFacadeTest : public ::testing::Test
{
protected:
	void SetUp() override
	{
		facade_ = std::make_unique<websocket_facade>();
	}

	void TearDown() override
	{
		// Cleanup any running clients/servers
		if (client_)
		{
			client_->stop();
		}
		if (server_)
		{
			server_->stop();
		}
	}

	std::unique_ptr<websocket_facade> facade_;
	std::shared_ptr<i_protocol_client> client_;
	std::shared_ptr<i_protocol_server> server_;
};

// ============================================================================
// Client Configuration Validation Tests
// ============================================================================

TEST_F(WebSocketFacadeTest, CreateClientThrowsOnNegativePingInterval)
{
	websocket_facade::client_config config{
		.ping_interval = std::chrono::milliseconds(-1000),
	};

	EXPECT_THROW(facade_->create_client(config), std::invalid_argument);
}

TEST_F(WebSocketFacadeTest, CreateClientThrowsOnZeroPingInterval)
{
	websocket_facade::client_config config{
		.ping_interval = std::chrono::milliseconds(0),
	};

	EXPECT_THROW(facade_->create_client(config), std::invalid_argument);
}

TEST_F(WebSocketFacadeTest, CreateClientWithDefaultPingInterval)
{
	websocket_facade::client_config config{};

	ASSERT_NO_THROW({
		client_ = facade_->create_client(config);
	});

	ASSERT_NE(client_, nullptr);
	EXPECT_FALSE(client_->is_connected());
}

